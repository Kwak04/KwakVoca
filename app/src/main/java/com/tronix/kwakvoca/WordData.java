package com.tronix.kwakvoca;

public class WordData {
    String word;
    String meaning;
    String user;
    String group;
    String uid;

    public String getWord() {
        return word;
    }

    public String getMeaning() {
        return meaning;
    }

    public String getUser() {
        return user;
    }

    public String getGroup() {
        return group;
    }

    public String getUid() {
        return uid;
    }
}
